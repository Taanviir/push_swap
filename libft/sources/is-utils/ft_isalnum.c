#include "libft.h"

// 0 returned if no alphanumeric char found and 1 returned if found
int	ft_isalnum(int c)
{
	return ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') \
		|| (c >= '0' && c <= '9'));
}
